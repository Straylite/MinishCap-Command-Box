function addFlag(addr, val) memory.writebyte(addr, bit.bor(memory.readbyte(addr), val)) end
function subFlag(addr, val) memory.writebyte(addr, bit.band(memory.readbyte(addr), bit.bnot(val))) end

function haveall()		
	memory.writebyte(0x03000FD2, 0xF8) -- White Transition
	memory.writebyte(0x030010AF, 0x00) -- No Cucco
	memory.writebyte(0x030010A8, 0x01) -- Initializing Teleport
	addFlag(0x02002B32, 0x54)
	addFlag(0x02002B33, 0x51)
	addFlag(0x02002B34, 0x55)
	addFlag(0x02002B35, 0x55)
	addFlag(0x02002B36, 0x54)
	addFlag(0x02002B37, 0x45)
	addFlag(0x02002B39, 0x55)
	addFlag(0x02002B42, 0x55)
	addFlag(0x02002B43, 0x55)
	addFlag(0x02002B4E, 0x55)
	addFlag(0x02002B4F, 0x5)
	addFlag(0x02002B44, 0x55)
	addFlag(0x02002B45, 0x55)
	addFlag(0x02002C9E, 0x10)	
end

function loseall()
	memory.writebyte(0x03000FD2, 0xF8) -- White Transition
	memory.writebyte(0x030010AF, 0x00) -- No Cucco
	memory.writebyte(0x030010A8, 0x01) -- Initializing Teleport
	subFlag(0x02002B32, 0x54)
	subFlag(0x02002B33, 0x51)
	subFlag(0x02002B34, 0x55)
	subFlag(0x02002B35, 0x55)
	subFlag(0x02002B36, 0x54)
	subFlag(0x02002B37, 0x45)
	subFlag(0x02002B39, 0x55)
	subFlag(0x02002B42, 0x55)
	subFlag(0x02002B43, 0x55)
	subFlag(0x02002B4E, 0x55)
	subFlag(0x02002B4F, 0x5)
	subFlag(0x02002B44, 0x55)
	subFlag(0x02002B45, 0x55)
	subFlag(0x02002C9E, 0x10)	
end